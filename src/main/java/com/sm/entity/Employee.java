package com.sm.entity;

public class Employee {
    private int employeeId;
    private String employeeName;
    private String employeeSex;
    private String employeeeTel;
    private String employeeeAdd;
    private String employeeeAt;

    public int getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getEmployeeSex() {
        return employeeSex;
    }

    public void setEmployeeSex(String employeeSex) {
        this.employeeSex = employeeSex;
    }

    public String getEmployeeeTel() {
        return employeeeTel;
    }

    public void setEmployeeeTel(String employeeeTel) {
        this.employeeeTel = employeeeTel;
    }

    public String getEmployeeeAdd() {
        return employeeeAdd;
    }

    public void setEmployeeeAdd(String employeeeAdd) {
        this.employeeeAdd = employeeeAdd;
    }

    public String getEmployeeeAt() {
        return employeeeAt;
    }

    public void setEmployeeeAt(String employeeeAt) {
        this.employeeeAt = employeeeAt;
    }
}
